#!/usr/bin/env node
/**
 * verify-coverage.mjs
 *
 * THE headline capability: deterministic completeness verification. Diffs the
 * manifest's candidate set (ground truth) against the items actually documented
 * in docs/unwind/layers/<layer>/*.md. Replaces the subjective LLM "did we miss
 * anything?" pass with reproducible set arithmetic — missing items come back
 * with names and line numbers.
 *
 * Usage:
 *   node verify-coverage.mjs <projectRoot> [manifestPath]
 *
 * Outputs:
 *   docs/unwind/.cache/coverage/{layer}.json   — full coverage report
 *   docs/unwind/layers/{layer}/gaps.md         — work list (only if gaps exist)
 *
 * Exit code 0 always (reporting tool); the orchestrator decides what to do with
 * the gaps. Layers with no docs folder report 0% (everything missing), which is
 * the correct signal that the layer is undocumented.
 */

import {
  existsSync,
  mkdirSync,
  readFileSync,
  readdirSync,
  writeFileSync,
} from "node:fs";
import { join, resolve } from "node:path";
import { loadCore } from "./_core.mjs";

const core = await loadCore();
const {
  candidatesByLayer,
  extractDocumentedItems,
  computeLayerCoverage,
  docDirsForLayer,
} = core;

const [, , projectRootArg, manifestArg] = process.argv;
if (!projectRootArg) {
  process.stderr.write("Usage: node verify-coverage.mjs <projectRoot> [manifestPath]\n");
  process.exit(1);
}
const projectRoot = resolve(projectRootArg);
const manifestPath = manifestArg
  ? resolve(manifestArg)
  : join(projectRoot, "docs/unwind/.cache/scan-manifest.json");
if (!existsSync(manifestPath)) {
  process.stderr.write(`verify-coverage: manifest not found: ${manifestPath}\nRun scan.mjs first.\n`);
  process.exit(1);
}

const manifest = JSON.parse(readFileSync(manifestPath, "utf-8"));
const byLayer = candidatesByLayer(manifest);

/** Read all markdown under a directory (recursive), excluding gaps.md. */
function readMarkdown(dir) {
  const out = [];
  let entries;
  try {
    entries = readdirSync(dir, { withFileTypes: true });
  } catch {
    return out;
  }
  for (const ent of entries) {
    const full = join(dir, ent.name);
    if (ent.isDirectory()) out.push(...readMarkdown(full));
    else if (ent.isFile() && ent.name.endsWith(".md") && ent.name !== "gaps.md") {
      out.push({ path: full, content: readFileSync(full, "utf-8") });
    }
  }
  return out;
}

const coverageDir = join(projectRoot, "docs/unwind/.cache/coverage");
mkdirSync(coverageDir, { recursive: true });

const layersRoot = join(projectRoot, "docs/unwind/layers");
const summary = [];

for (const [layer, candidates] of Object.entries(byLayer)) {
  // A layer may map to several doc folders (tests -> unit/integration/e2e). Read
  // markdown across ALL of them and union the documented items so a single
  // scanner layer documented across multiple specialist folders still resolves.
  const dirNames = docDirsForLayer(layer);
  const docDirs = dirNames.map((d) => join(layersRoot, d));
  const existingDirs = docDirs.filter((d) => existsSync(d));
  const documented = existingDirs
    .flatMap((d) => readMarkdown(d))
    .flatMap((f) => extractDocumentedItems(f.content, f.path.replace(projectRoot + "/", "")));
  const cov = computeLayerCoverage(layer, candidates, documented);

  writeFileSync(join(coverageDir, `${layer}.json`), JSON.stringify(cov, null, 2), "utf-8");

  // Defensive: a non-empty layer with NO doc folder on disk is a different
  // failure mode than a genuinely undocumented one — it usually means the
  // specialist wrote to a different folder than the verifier expects. Surface it
  // explicitly so a folder-name mismatch can't masquerade as a silent 0%.
  if (cov.total > 0 && existingDirs.length === 0) {
    process.stderr.write(
      `verify-coverage: WARNING ${layer}: no doc dir found (expected one of ` +
        `${dirNames.map((d) => `layers/${d}/`).join(", ")}) — did the specialist write elsewhere?\n`,
    );
  }

  // Emit a gaps.md work list (in the layer's primary existing folder) only when
  // there are missing items and the layer is actually being documented. Skip
  // undocumented layers so we don't litter gaps.md into every empty layer.
  if (cov.missing.length > 0 && existingDirs.length > 0) {
    writeFileSync(join(existingDirs[0], "gaps.md"), renderGaps(cov), "utf-8");
  }

  summary.push({ layer, ...cov });
}

// Print a compact, deterministic summary table to stderr.
process.stderr.write("verify-coverage: layer coverage (covered/total = pct%)\n");
for (const s of summary.sort((a, b) => a.layer.localeCompare(b.layer))) {
  const flag = s.coveragePct < 100 && s.total > 0 ? "  <-- gaps" : "";
  process.stderr.write(
    `  ${s.layer.padEnd(15)} ${String(s.covered).padStart(4)}/${String(s.total).padEnd(4)} = ` +
      `${String(s.coveragePct).padStart(5)}%  (byId=${s.matchedById} fuzzy=${s.matchedByFuzzy})${flag}\n`,
  );
}

function renderGaps(cov) {
  const lines = [
    `# ${cov.layer} Documentation Gaps`,
    "",
    `> Generated by verify-coverage. Coverage: ${cov.covered}/${cov.total} (${cov.coveragePct}%).`,
    "> These items exist in source (per the deterministic scan) but are not yet documented.",
    "",
    "## Missing Items",
    "",
  ];
  for (const m of cov.missing) {
    lines.push(`### ${m.name}`);
    lines.push(`- **Type:** ${m.kind}`);
    lines.push(`- **Location:** ${m.file}:${m.startLine}-${m.endLine}`);
    lines.push(`- **Id:** \`${m.id}\``);
    lines.push("");
  }
  if (cov.extra.length > 0) {
    lines.push("## Documented but not in scan (review — may be dynamic or stale)");
    lines.push("");
    for (const e of cov.extra) lines.push(`- ${e}`);
    lines.push("");
  }
  return lines.join("\n");
}
